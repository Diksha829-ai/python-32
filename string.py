

my_string = "Hello, World!"
print("Original string:", my_string)


print("Character at index 0:", my_string[0])
print("Character at index 1:", my_string[1])
print("Character at index 7:", my_string[7])


print("Character at index -1 (last):", my_string[-1])
print("Character at index -2:", my_string[-2])
print("Character at index -7:", my_string[-7])

print("Characters from index 1 to 5 (1:6):", my_string[1:6])
print("Characters from index -12 to -7 (-12:-7):", my_string[-12:-7])
